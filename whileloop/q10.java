package whileloop;

// WAP to print all the even numbers from 1 to 100.

public class q10 {
    public static void main(String[] args) {
        int i = 1 ;
        while(i<=100){
            if (i%2==0) {
                System.out.println(i);
                
            }
            
            i++;
        }
    }
    
}
